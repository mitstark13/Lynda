$('.searchCom input').keypress(function() {
	$('.comDrop').css('display', 'block');
});
$('.searchCom input').mouseleave(function() {
	$('.comDrop').css('display', 'none');
})
$('.comDrop').mouseleave(function() {
	$('.comDrop').css('display', 'none');
})
$('.comDrop').mouseenter(function() {
	$('.comDrop').css('display', 'block');
})